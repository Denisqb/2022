        void matrix3(int n, int arr1[], int arr2[], int arr3[])
        {
            int f = 0;
		    for(int i=0; i < n*n; i++)
		    {
		    	int x = 0;
				arr3[i] = 0;
				if(i%n == 0  && i != 0)
				{
					f++;
				}

				for(int k = 0; k < n; k++)
		    	{
			        x = arr1[k+ n*f ] * arr2[i + n*(k - f)];
			        arr3[i] = arr3[i] + x;
			    }
		    }

            printf("\nРезультат множення першої матриці на другу:\n");

			for (int i=0; i < n; i++)
			{
				int j = 0;
				for (j = 0; j < n; j++)
				{
				printf("%d ", arr3[j + n*i ]);
				}
				printf("\n");
			}
        }
