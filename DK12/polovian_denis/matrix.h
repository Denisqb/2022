        void matrix1(int n, int arr1[])
        {
            printf("\nПерша матриця:\n");
			for (int i=0; i < n; i++)
			{
				int j = 0;
				for (j = 0; j < n; j++)
				{
				printf("%d ", arr1[j + n*i ]);
				}
				printf("\n");
			}
        }

